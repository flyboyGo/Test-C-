#include <iostream>

using namespace std;

int main(int argc, char *argv[])
{

    //C++的三大特性
    //1、封装
       //把客观的事物抽象成一个类(将数据与方法打包在一起,加以权限的区分,达到保护并安全使用数据的目的)

    //2、继承
       //继承表达的是类之间的关系,这种关系使得对象可以继承另外一个类对象的特征和能力
       //目的:避免共公用代码的重复开发,减少代码和数据的冗余

    //3、多态
       //多态 多态性可以简单地概括为“一个接口，多种方法”，字面意思为多种形态。
       //程序在运行时才决定调用的函数,它是面向对象编程领域的核心概念。

    cout << "Hello World!" << endl;
    return 0;
}
